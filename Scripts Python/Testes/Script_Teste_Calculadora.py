v1 = int(input('Digite um valor '))
v2 = int(input('Digite outro valor '))

soma = v1 + v2
subtracao = v1 - v2
multiplicacao = v1 * v2
divisao = v1 / v2
exponenciacao = v1 ** v2

print(f'A soma entre {v1} e {v2} é {soma}')
print(f'A subtração entre {v1} e {v2} é {subtracao}')
print(f'A multiplicação entre {v1} e {v2} é {multiplicacao}')
print(f'A divisão entre {v1} e {v2} é {divisao:.3f}')
print(f'A exponenciação entre {v1} e {v2} é {exponenciacao}')
